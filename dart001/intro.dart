void main() {
  variaveisComInferencia();
  variaveisComTipagemEstatica();
}

void variaveisComInferencia() {
  // Variáveis com inferência de tipos semelhante ao TypeScript

  var nome = 'Viajante I';
  var idade = 22;

  print(nome);
  print(idade);
}

void variaveisComTipagemEstatica() {
  // Variáveis com tipos declarados

  String nome2 = 'Viajante II';
  int idade2 = 22;

  print(nome2);
  print(idade2);
}